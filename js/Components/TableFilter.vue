<template>
  <ButtonWithDropdown
    placement="bottom-end"
    dusk="filters-dropdown"
    :pre-style="preStyle"
  >
    <template #button>
      <svg
        xmlns="http://www.w3.org/2000/svg"
        class="h-5 w-5 text-gray-400"
        viewBox="0 0 20 20"
        fill="currentColor"
      >
        <path
          fill-rule="evenodd"
          d="M3 3a1 1 0 011-1h12a1 1 0 011 1v3a1 1 0 01-.293.707L12 11.414V15a1 1 0 01-.293.707l-2 2A1 1 0 018 17v-5.586L3.293 6.707A1 1 0 013 6V3z"
          clip-rule="evenodd"
        />
      </svg>
      <span v-if="hasEnabledFilters" class="ml-1">({{ activeFiltersCount }})</span>
    </template>

    <div
      role="menu"
      aria-orientation="horizontal"
      aria-labelledby="filter-menu"
      class="min-w-max"
    >
      <div
        v-for="(filter, key) in filters"
        :key="key"
      >
        <h3 class="text-xs uppercase tracking-wide bg-gray-100 p-3">
          {{ filter.label }}
        </h3>
        <div class="p-2">
          <select
            v-if="filter.type === 'select'"
            :name="filter.key"
            :value="filter.value"
            :class="getTheme('select', preStyle)"
            @change="onFilterChange(filter.key, $event.target.value)"
          >
            <option
              v-for="(option, optionKey) in filter.options"
              :key="optionKey"
              :value="optionKey"
            >
              {{ option }}
            </option>
          </select>
          <ToggleFilter
            v-if="filter.type === 'toggle'"
            :filter="filter"
            :on-filter-change="onFilterChange"
            :pre-style="preStyle"
          />
          <div v-if="filter.type === 'number_range'" class="py-4 px-8" style="min-width: 250px;">
            <NumberRangeFilter
              :max="filter.max"
              :min="filter.min"
              :prefix="filter.prefix"
              :suffix="filter.suffix"
              :step="filter.step"
              :pre-style="preStyle"
              v-model="filter.value"
              @update:model-value="updateNumberRangeFilter(filter)"
            />
          </div>
        </div>
      </div>
    </div>
  </ButtonWithDropdown>
</template>

<script setup>
import ButtonWithDropdown from "./ButtonWithDropdown.vue";
import {computed, inject, ref} from "vue";
import ToggleFilter from "./TableFilters/ToggleFilter.vue";
import NumberRangeFilter from "./TableFilters/NumberRangeFilter.vue";

const props = defineProps({
    hasEnabledFilters: {
        type: Boolean,
        required: true,
    },

    filters: {
        type: Object,
        required: true,
    },

    onFilterChange: {
        type: Function,
        required: true,
    },

    preStyle: {
        type: String,
        default: 'default',
        required: false,
    },
});

const timeout = ref(null)

const activeFiltersCount = computed(() => {
  return props.filters.filter((f) => f.value === null).length
})

function updateNumberRangeFilter(filter) {
  if (timeout.value) {
    clearTimeout(timeout.value)
  } timeout.value = setTimeout(() => {
    props.onFilterChange(filter.key, filter.value)
  }, 500)
}

// Theme
const commonSelectClasses = "block w-full shadow-sm text-sm border-gray-300 rounded-md"

const fallbackTheme = {
    inertia_table: {
        table_filter: {
            select: {
                default: `${commonSelectClasses} focus:ring-indigo-500 focus:border-indigo-500`,
                dootix: `${commonSelectClasses} focus:ring-cyan-500 focus:border-blue-500`,
            },
        },
    },
}
const themeVariables = inject('themeVariables');
const getTheme = (type, name) => {
    if (
        "inertia_table" in themeVariables &&
        "table_filter" in themeVariables.inertia_table &&
        type in themeVariables.inertia_table.table_filter &&
        name in themeVariables.inertia_table.table_filter[type]
    ) {
        return themeVariables.inertia_table.table_filter[type][name];
    } else {
        return fallbackTheme.inertia_table.table_filter[type][name];
    }
}
</script>

